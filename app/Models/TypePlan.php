     */
    public $timestamps = true;

    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'name',
        'message',
        'status',
    ];

    /**
     * The attributes that should be casted to native types.
     *
     * @var array
     */
